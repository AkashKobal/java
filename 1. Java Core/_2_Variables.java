public class _2_Variables {
    public static void main(String[] args) {

        // Integer variables
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(a);
        System.out.println(b);

        System.out.println(c);

        // String variable
        String name = "John";
        System.out.println(name);

    }

}
